package com.edu.switch_string;

public class StringSwitch {

	public static void main(String[] args) {
		String s = "h";
		
		switch(s){
			case "h": System.out.println("hey you passed 'h', this is a java 7 new feature ");
			break;
			default:
				System.out.println("String passing is not valid");
		
		
		}
	}

}
